(def splitted-lines (clojure.string/split-lines input))

(defn compute-trees [lines position sum move line-len down]
(let []
    (if (empty? lines)
      sum
      (if (= (str (nth (clojure.string/trim (first lines)) position)) "#")
        (compute-trees (down lines) (mod (+ position move) line-len) (+ sum 1) move line-len down)
        (compute-trees (down lines) (mod (+ position move) line-len) sum move line-len down)))))

(defn skip-lines [lines]
  (rest (rest lines)))

(* (compute-trees splitted-lines 0 0 1 (count (clojure.string/trim (first splitted-lines))) rest)
  (compute-trees splitted-lines 0 0 3 (count (clojure.string/trim (first splitted-lines))) rest)
  (compute-trees splitted-lines 0 0 5 (count (clojure.string/trim (first splitted-lines))) rest)
  (compute-trees splitted-lines 0 0 7 (count (clojure.string/trim (first splitted-lines))) rest)
   (compute-trees splitted-lines 0 0 1 (count (clojure.string/trim (first splitted-lines))) skip-lines))
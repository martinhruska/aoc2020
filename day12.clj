(defrecord State [noso weea angle act])

(defn addDir [d1 d2 dir old val]
(let [conv (if (or (= dir "S") (= "W" dir)) -1 1)
      dirVal (* conv val)]
    (if (or (= dir d1) (= dir d2)) (+ dirVal old) old)))

(defn newAct [angle]
    (cond 
        (and (>= angle 315) (< angle 360)) "N"
        (and (>= angle 0) (< angle 45)) "N"
        (and (>= angle 45) (< angle 135)) "E"
        (and (>= angle 135) (< angle 225)) "S"
        (and (>= angle 225) (< angle 315)) "W"
        :else false))

(defn computeAngle [old val]
(let [new (+ old val)]
     (if (< new 0)
       (+ 360 new)
       (mod new 360))))

(defn applyInstr [instr state]
(let [op (first instr)
      val (second instr)
      noso (:noso state)
      weea (:weea state)
      angle (:angle state)
      act (:act state)]
    (case op
        "N" (State. (+ val noso) weea angle act)
        "S" (State. (+ val noso) weea angle act)
        "W" (State. noso (+ val weea) angle act)
        "E" (State. noso (+ val weea) angle act)
        "F" (State. (addDir "N" "S" act noso val) (addDir "W" "E" act weea val) angle act)
        "L" (State. noso weea (computeAngle angle val) (newAct (computeAngle angle val)))
        "R" (State. noso weea (computeAngle angle val) (newAct (computeAngle angle val)))
        :else false)))

